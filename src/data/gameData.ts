import type { CharacterData, MapData, Attack } from '../types/game';

const createAttack = (
  name: string,
  damage: number,
  range: number,
  knockback: number,
  type: 'punch' | 'kick' | 'special',
  speed: 'fast' | 'medium' | 'slow'
): Attack => {
  const speedConfig = {
    fast: { startup: 3, active: 4, recovery: 8 },
    medium: { startup: 5, active: 5, recovery: 12 },
    slow: { startup: 8, active: 6, recovery: 16 },
  };
  // Specials are slower but more powerful
  const specialConfig = {
    fast: { startup: 6, active: 6, recovery: 14 },
    medium: { startup: 10, active: 8, recovery: 18 },
    slow: { startup: 14, active: 10, recovery: 22 },
  };
  const config = type === 'special' ? specialConfig[speed] : speedConfig[speed];
  return {
    name,
    damage,
    range,
    knockback,
    type,
    sound: type === 'punch' ? 'WHAM!' : type === 'kick' ? 'POW!' : 'BOOM!',
    ...config,
  };
};

